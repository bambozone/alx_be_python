

   
def perform_operation(num1, num2, operation):
    if operation == "add":
       return num1 + num2
    if operation == "subtract":
       return num1 - num2
    if operation == "multiply":
       return num1 * num2
    if operation == "divide":
       if num1 == 0 :
         print ("cannot divide by zero")
       else :
          return num1 / num2 
   
    









    